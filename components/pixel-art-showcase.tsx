interface PixelSpriteProps {
  sprite: number[][]
  palette: string[]
  pixelSize?: number
  className?: string
}

export function PixelSprite({ sprite, palette, pixelSize = 4, className = "" }: PixelSpriteProps) {
  const renderedSprite = useMemo(() => {
    return sprite.flat().map((colorIndex, i) => ({
      key: i,
      color: palette[colorIndex] || "transparent",
    }))
  }, [sprite, palette])

  return (
    <div 
      className={`inline-grid ${className}`}
      style={{ 
        gridTemplateColumns: `repeat(${sprite[0].length}, ${pixelSize}px)`,
        gap: 0,
        imageRendering: "pixelated",
      }}
    >
      {renderedSprite.map(({ key, color }) => (
        <div
          key={key}
          style={{
            width: pixelSize,
            height: pixelSize,
            backgroundColor: color,
          }}
        />
      ))}
    </div>
  )
}

// Animated sprite with multiple frames
interface AnimatedSpriteProps {
  frames: number[][][]
  palette: string[]
  pixelSize?: number
  frameRate?: number
  className?: string
}

export function AnimatedSprite({ 
  frames, 
  palette, 
  pixelSize = 4, 
  frameRate = 500,
  className = "" 
}: AnimatedSpriteProps) {
  const [currentFrame, setCurrentFrame] = useState(0)
  
  useEffect(() => {
    const interval = setInterval(() => {
      setCurrentFrame(f => (f + 1) % frames.length)
    }, frameRate)
    return () => clearInterval(interval)
  }, [frames.length, frameRate])

  return (
    <PixelSprite 
      sprite={frames[currentFrame]} 
      palette={palette} 
      pixelSize={pixelSize}
      className={className}
    />
  )
}

